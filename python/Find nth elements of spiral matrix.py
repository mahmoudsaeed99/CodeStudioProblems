def findKthElement(matrix, k):
    n = len(matrix)
    m = len(matrix[0])
    r = m-1
    t = 0
    b = n-1
    l = -1
    k = k - 1
    i = 0
    j = 0
    while k >= 0:
        print(b , t , r , l , k)
        if k > r :
            k = k-r
            j = r
            l +=1
        else:
            if l == -1:
                l = 0 
            j = l + k
            break
        print(b , t , r , l , k)
        if k > b:
            k = k - (b-t)
            i = b
            t += 1
        else:
            i = t + k
            break
        print(b , t , r , l , k)
        if k > r - l:
            k = k - (r - l)
            j = l
            r -= 1
        else:
            j = r - k 
            break
        
        print(b , t , r , l , k)
        if k > (b - t):
            k = k - (b - t)
            i = t
            b -= 1
        else:
            
            i = b - k
            break
        
    return matrix[i][j]


